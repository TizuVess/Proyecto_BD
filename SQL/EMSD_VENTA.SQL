CREATE TABLE EMSD_VENTA(
    COD_VENTA NUMBER,
    COD_REPUESTO NUMBER,
    COD_CLIENTE NUMBER,
    PK_EMSD_VENTA PRIMARY KEY(COD_VENTA),
    FK_EMSD_VENTA_REPUESTOS FOREIGN KEY(COD_REPUESTO) REFERENCES EMSD_REPUESTOS(COD_REPUESTO),
    FK_EMSD_VENTA_CLIENTES FOREIGN KEY(COD_CLIENTE) REFERENCES EMSD_CLIENTES(COD_CLIENTE)
);