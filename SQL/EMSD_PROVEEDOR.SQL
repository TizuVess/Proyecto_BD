CREATE TABLE EMSD_PROVEEDOR(
    COD_PROVEEDOR NUMBER,
    NOMBRE_PROVEEDOR VARCHAR2(80),
    NRO_CONTACTO NUMBER,
    PK_EMSD_PROVEEDOR PRIMARY KEY(COD_PROVEEDOR)
);

CREATE OR REPLACE TRIGGER EMSD_PROVEEDOR
BEFORE INSERT
ON EMSD_PROVEEDOR
FOR EACH ROW
BEGIN
	
END;

CREATE OR REPLACE PROCEDURE EMSD_PROVEEDOR_(


)
IS 
BEGIN
	LOCK TABLE EMSD_PROVEEDOR IN ROW EXCLUSIVE MODE;

	COMMIT;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
    		RAISE_APPLICATION_ERROR(+100, 'No existe este PROVEEDOR');
    	WHEN STORAGE_ERROR THEN
            RAISE_APPLICATION_ERROR(-6500, 'Fallo de memoria');
    ROLLBACK;
END;