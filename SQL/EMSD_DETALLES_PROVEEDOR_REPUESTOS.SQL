CREATE TABLE EMSD_DETALLES_PROVEEDOR_REPUESTOS(
    COD_PROVEEDOR NUMBER,
    COD_REPUESTO NUMBER,
    PK_EMSD_DETALLES_PROVEEDOR_REPUESTOS PRIMARY KEY(COD_PROVEEDOR, COD_REPUESTO),
    FK_EMSD_DETALLES_PROVEEDOR_REPUESTOS_PROVEEDOR FOREIGN KEY(COD_PROVEEDOR) REFERENCES EMSD_PROVEEDOR(COD_PROVEEDOR),
    FK_EMSD_DETALLES_PROVEEDOR_REPUESTOS_REPUESTOS FOREIGN KEY(COD_REPUESTO) REFERENCES EMSD_REPUESTOS(COD_REPUESTO)
);