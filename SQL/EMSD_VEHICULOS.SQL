CREATE TABLE EMSD_VEHICULOS(
    VIN_VEHICULO VARCHAR2(17),
    COD_CLIENTE NUMBER,
    COD_MARCA_VEHICULO NUMBER,
    COD_ANIO_FABRICACION NUMBER,
    PATENTE VARCHAR2(6),
    PK_EMSD_VEHICULOS PRIMARY KEY(VIN_VEHICULO),
    FK_EMSD_VEHICULOS_CLIENTES FOREIGN KEY(COD_CLIENTE) REFERENCES EMSD_CLIENTES(COD_CLIENTE),
    FK_EMSD_VEHICULOS_MARCAS_VEHICULOS FOREIGN KEY(COD_MARCA_VEHICULO) REFERENCES EMSD_MARCAS_VEHICULOS(COD_MARCA_VEHICULO),
    FK_EMSD_VEHICULOS_ANIO_FABRICACION_VEHICULO FOREIGN KEY(COD_ANIO_FABRICACION) REFERENCES EMSD_ANIO_FABRICACION_VEHICULO(COD_ANIO_FABRICACION)
);

CREATE OR REPLACE TRIGGER EMSD_VEHICULOS
BEFORE INSERT
ON EMSD_VEHICULOS
FOR EACH ROW
BEGIN
	
END;

CREATE OR REPLACE PROCEDURE EMSD_VEHICULOS_(


)
IS 
BEGIN
	LOCK TABLE EMSD_VEHICULOS IN ROW EXCLUSIVE MODE;

	COMMIT;
	EXCEPTION
		WHEN NO_DATA_FOUND THEN
    		RAISE_APPLICATION_ERROR(+100, 'No existe');
    	WHEN STORAGE_ERROR THEN
            RAISE_APPLICATION_ERROR(-6500, 'Fallo de memoria');
    ROLLBACK;
END;