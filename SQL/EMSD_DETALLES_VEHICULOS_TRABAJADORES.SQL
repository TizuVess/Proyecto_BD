CREATE TABLE EMSD_DETALLES_VEHICULOS_TRABAJADORES(
    VIN_VEHICULO NUMBER,
    COD_TRABAJADOR NUMBER,
    PK_EMSD_DETALLES_VEHICULOS_TRABAJADORES PRIMARY KEY(VIN_VEHICULO,COD_TRABAJADOR),
    FK_EMSD_DETALLES_VEHICULOS_TRABAJADORES_VEHICULOS FOREIGN KEY(VIN_VEHICULO) REFERENCES EMSD_VEHICULOS(VIN_VEHICULO),
    FK_EMSD_DETALLES_VEHICULOS_TRABAJADORES_TRABAJADORES FOREIGN KEY(COD_TRABAJADOR) REFERENCES EMSD_TRABAJADORES(COD_TRABAJADOR)
);